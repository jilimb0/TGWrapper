import { mkdirSync, writeFileSync } from 'node:fs';
import { resolve } from 'node:path';

const args = process.argv.slice(2);

function readArg(name) {
  const prefix = `${name}=`;
  const found = args.find((arg) => arg.startsWith(prefix));
  return found ? found.slice(prefix.length) : '';
}

const updates = readArg('--updates')
  .split(',')
  .map((value) => value.trim())
  .filter(Boolean);

const methods = readArg('--methods')
  .split(',')
  .map((value) => value.trim())
  .filter(Boolean);

const version = readArg('--version') || 'unknown';

if (updates.length === 0 && methods.length === 0) {
  console.error(
    'Usage: node scripts/generate-telegram-type-stubs.mjs --version=9.5 --updates=gift,owned_gifts --methods=getAvailableGifts'
  );
  process.exit(1);
}

const outDir = resolve(process.cwd(), 'docs/generated');
const outFile = resolve(outDir, `telegram-type-stubs-${version.replace(/\./g, '_')}.md`);

mkdirSync(outDir, { recursive: true });

const updateStubs = updates
  .map(
    (key) => `  ${key}?: {
    // TODO: add concrete Bot API ${version} fields
    [key: string]: unknown;
  };`
  )
  .join('\n');

const methodStubs = methods
  .map(
    (name) => `  ${name}: (payload: {
    // TODO: add concrete Bot API ${version} payload fields
    [key: string]: unknown;
  }) => T;`
  )
  .join('\n');

const content = `# Telegram Type Stubs (Bot API ${version})

Generated by \`scripts/generate-telegram-type-stubs.mjs\`.
Use this file as a manual patch helper for:
- \`/Users/jilimbo/Documents/Personal/TGWrapper/src/types/telegram.ts\`
- compatibility tests and docs.

## Update stubs

\`\`\`ts
export interface Update {
${updateStubs || '  // no update stubs requested'}
}
\`\`\`

## ApiMethods stubs

\`\`\`ts
export type ApiMethods<T = unknown> = {
${methodStubs || '  // no method stubs requested'}
};
\`\`\`
`;

writeFileSync(outFile, content, 'utf8');

console.log(
  JSON.stringify(
    {
      status: 'ok',
      output: outFile,
      version,
      updates,
      methods
    },
    null,
    2
  )
);
